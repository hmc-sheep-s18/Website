                        injuries but that would be ridiculous and contribute to conspiracy theory culture. Maybe we make more players like shohei ohtani 
                        and stop making pitchers only pitchers. Only playing when you have to throw at the maximum intensity on the mound is also enticing 
                        to study further. Is that better than throwing the ball over from second base every game? I’d like data to explore that. Would 
