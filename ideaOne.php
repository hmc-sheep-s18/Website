                    <h3><b>A Changing Sport Amongst the Youth</b>  <span class="w3-opacity">Last Updated: July 24, 2018</span></h3>
                    <h5>It's no longer about the sport</h5>
                    
                    <p> with young athletes playing in Southern California, in my humble opinion. Trigger Warning: 
                        If you are not willing to keep an open mind because you might fit within the 
                        demographic that is causing the problems laid out in this piece, then make your 
                        own website and exit out of your browser. The environment that is youth travel ball 
                        and wreck leagues has grown into a major problem that has had severe consequences for 
                        everyone involved.  There are some real problems with the 
                        increasingly more lucrative business of youth baseball that need to be solved, and 
                        any resistance to the presentation of the idea will further the problem and delay a 
                        solution. Now that we have that out of the way, we could get to the real issues.</p>


                    <p> I'm not going to talk about the idea of having children play sports at a very young 
                        age for fun, I think that there are plenty of benefits that come out of team sports
                        and outside physical activity. It all starts at that awkward transitional phase of youth
                        baseball right around the 7th-8th grades before highschool. There is a looming cloud
                        over the heads of the young athletes signaling that they might not be able to play the sport 
                        they grew up playing anymore. Specifically, this cloud was the upcoming tryouts for making 
                        it onto the freshmen highschool baseball team. Anecdotally it became increasingly 
                        less about the game and more about myself being able to continue playing the game. And, I was
                        all for continuing playing the game for as long as I could. I genuinely enjoyed competing with 
                        other, really good baseball players for my age group. This pressure and demand to be on the 
                        freshman baseball program at the local highschool was so great that it was clear to everyone
                        that there were only so many spots on the team. Enter the hundreds of youth 13u travel ball 
                        teams that claim to play at a more competitive level of play to get your kid ready to play 
                        highschool baseball. Enter the drama that came with not playing as much as you wanted to. This 
                        drama and complaining about playing time was only a realization of "Wow, I'm actually not as
                        good as my parents claim me to be" and insecurity of that as a result. So as the demand 
                        for being on the best team in your area began to skyrocket and some of the "daddy ball"
                        managers of these teams began to realize an economic opportunity that they could do on the 
                        side of their career. People were willing to pay whatever and to whoever to "gaurentee"
                        their son's spot on the local highschool program to keep their son from knowing that other 
                        players could possibly be more skilled and talented than them. Insert what is wrong with America
                        comment here amiright? Contrary to their beliefs, it actually required some dedication and 
                        work to get what they wanted. Unfortunately for those, freshman cut-day was too late for 
                        this to come to their fruition. Enter part two of this deboucle, because a pattern is about 
                        to emerge in high school baseball.</p>
                        
                    <p> Wheew, so you made it through freshman cuts huh? This means you have four years of breathing room 
                        before that pressure will creep up on you again with a bit more intensity, stay tuned. Now you'll experience the 
                        initial awkwardness of seeing people on your drama-filled travel ball team that no longer play baseball
                        and see them adjust to life without a sport they've been playing for the last 7 years of their lives. 
                        It looks awful, so you vow to work harder than everyone to avoid going through it. For some, this promise
                        will break next week and they'll be part of the next cut or placed on the JV team if they're lucky. Throughout 
                        the next couple years of highschool you'll have a lot of fun, experience a lot of life changing lessons on and off 
                        the field with your teammates, and start to develop a sense of identity. All the good stuff, you know what I mean. 
                        Then talk of this thing you haven't been giving much thought to rears it's ugly head, college. Yes, there 
                        will be stress for trying to get the best grades like all of the non-baseball players at your school and somehow still 
                        play one of the hardest games on the planet better than the 500,000 other high school baseball players. The parents of
                        these players also have learned that they could dodge the rising tuitions across America by getting a baseball
                        scholarship. This is almost like the travel ball days! Except there's a lot more money involved and 
                        I would argue going to college, and where you go to college could play a significant role in what you 
                        do later in life. So this no doubtedly creates pressure on the player to be accepted by his peers as a 
                        serious amatuer baseball player and to also be acknowledged by his parents as someone who is attempting to
                        help with family finances. Now, yes you should go to college wherever you would like, if you even choose to. 
                        Baseball should not have any influence on where you should go. However, college coaches will not risk their
                        job recruiting someone who's not 100% there to play baseball anyways. 

                        So here's the real problem. All of this time, energy, and money spent trying to get to the 
                        next level has probably started to take it's toll on your body. Maybe a few minor tendonitis problems and
                        maybe a surgery is thrown into your lap. Now what? Well, more money spent paying for the surgery if insurance 
                        does not cover everything. Now you feel the chance to play college or professional baseball slowly start slipping away.
                        After physical therapy you kick it into overdrive, working your body to it's limit, risking injury again. All of this 
                        stress on the arm is undoubtedly from the year-round play with little rest since you were in little league. After 10+ years 
                        of unnecessary year round play that you thought was getting you better, and hopefully it did in some aspects, your body 
                        begins to break down. Specialization in one sport is becoming more and more common, since baseball players are only getting 
                        better because of it. However, more of them are having throwing related surgeries now more than ever as well. 
                        
